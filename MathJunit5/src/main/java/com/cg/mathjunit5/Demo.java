package com.cg.mathjunit5;

public class Demo {
	public int add(int a,int b) {
		return a+b;
	}
	public int mul(int a,int b)
	{
		return a*b;
	}
	public int div(int a,int b)
	{
		return a/b;
	}
	public double areaOfcircle(Double radius)
	{
		return Math.PI*radius*radius;
	}
	public boolean isOdd(int num)
	{
		return num %2 != 0;
	}
}
